    {
        $chart1 = Charts::database(Student::all(),'bar', 'highcharts')
            ->setTitle('Selected Students for the Degree Programme in past years')
            ->groupBy('batch')
            ->setElementLabel('Number of Students')
            ->setWidth(0)
            ->setHeight(450)
            ->setResponsive(false);

        return view('admin.index', compact('chart1'));
